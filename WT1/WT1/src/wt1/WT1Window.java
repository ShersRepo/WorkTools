            setting.fill = GridBagConstraints.HORIZONTAL;
            setting.gridx = 0;
            setting.gridy = 3;
            setting.ipadx = 200;
            buttonPanel.add(customerField,setting);
            

            splitter.setLeftComponent(scrollList);
            splitter.setRightComponent(buttonPanel);
            
            customerField.setSize(500, 25);
